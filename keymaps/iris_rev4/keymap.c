void handle_encoder_mode(bool clockwise) {
  layer_state_t layer = biton32(layer_state);
  volatile uint16_t next_encoder_mode = encoder_mode;

  // Alternate encoder mode (like shift encoder)
  if (layer == RAISE_LAYER) {
    next_encoder_mode = ENC_ARROWS_V;
  }

  if (layer == LOWER_LAYER) {
    next_encoder_mode = ENC_MS_WH;
  }

  switch (next_encoder_mode) {
    case ENC_VOL:
      if (clockwise) {
        tap_code(KC_VOLU);
      } else {
        tap_code(KC_VOLD);
      }
      break;
    case ENC_MS_WH:
      if (clockwise) {
        tap_code(KC_MS_WH_DOWN);
      } else {
        tap_code(KC_MS_WH_UP);
      }
      break;
    case ENC_ARROWS_V:
      if (clockwise) {
        tap_code(KC_UP);
      } else {
        tap_code(KC_DOWN);
      }
      break;
    case ENC_ARROWS_H:
      if (clockwise) {
        tap_code(KC_RIGHT);
      } else {
        tap_code(KC_LEFT);
      }
      break;
    case ENC_UNDO:
      if (clockwise) {
        tap_code16(G(KC_Y));
      } else {
        tap_code16(G(KC_Z));
      }
      break;
    case ENC_RGB_HUE:
      if (clockwise) {
        rgblight_increase_hue();
      } else {
        rgblight_decrease_hue();
      }
      break;
    case ENC_RGB_MODE:
      if (clockwise) {
        rgblight_step();
      } else {
        rgblight_step_reverse();
      }
      break;
    case ENC_RGB_VAL:
      if (clockwise) {
        rgblight_increase_val();
      } else {
        rgblight_decrease_val();
      }
      break;
  }
}
